// Подключаем библиотеку для работы с вводом/выводом
// iostream - input/output stream, позволяет выводить данные в консоль и читать из нее
#include <iostream>

// Главная функция программы - точка входа
// int main() - функция, с которой начинается выполнение программы
// int - тип возвращаемого значения (код завершения программы)
int main()
{
    // Цикл for - классическая форма цикла в C++
    // Синтаксис: for (начальное_действие; условие; действие_в_конце_итерации)
    // int num = 1 - объявляем переменную num типа int и инициализируем значением 1
    // num <= 100 - условие продолжения цикла: пока num меньше или равно 100
    // num += 1 - действие после каждой итерации: увеличиваем num на 1
    // Оператор += это сокращение от num = num + 1
    for (int num = 1; num <= 100; num += 1)
    {
        // Проверяем, кратно ли число и 3, и 5 одновременно
        // Оператор % - деление с остатком (модуль)
        // num % 15 == 0 означает, что остаток от деления num на 15 равен 0
        // Если число кратно 15, то оно кратно и 3, и 5 одновременно
        if (num % 15 == 0)
        {
            // Выводим "FizzBuzz" если число кратно 15
            // std::cout - стандартный поток вывода (console output)
            // << - оператор вставки, отправляет данные в поток
            // std::endl - переводит курсор на новую строку и сбрасывает буфер
            std::cout << "FizzBuzz" << std::endl;
        }
        // else if - альтернативное условие, проверяется только если предыдущее условие ложно
        // Проверяем, кратно ли число 3
        else if (num % 3 == 0)
        {
            // Выводим "Fizz" если число кратно 3
            std::cout << "Fizz" << std::endl;
        }
        // Проверяем, кратно ли число 5
        else if (num % 5 == 0)
        {
            // Выводим "Buzz" если число кратно 5
            std::cout << "Buzz" << std::endl;
        }
        // else - блок выполняется, если все предыдущие условия ложны
        else
        {
            // Выводим само число, если оно не кратно ни 3, ни 5
            std::cout << num << std::endl;
        }
    }
    // Возвращаем 0 - код успешного завершения программы
    // В C++ можно не писать return 0; в конце main(), компилятор добавит автоматически
    return 0;
}
