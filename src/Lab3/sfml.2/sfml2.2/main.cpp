// Подключаем библиотеку SFML для работы с графикой
#include <SFML/Graphics.hpp>
// Подключаем библиотеку для математических функций
#include <cmath>
// Определяем константу Пи, если она не определена
#ifndef M_PI
#define M_PI 3.14159265358979323846
#endif

// Главная функция программы
int main()
{
    // === СОЗДАНИЕ ОКНА С ПАРАМЕТРАМИ СГЛАЖИВАНИЯ ===
    // sf::ContextSettings - настройки контекста OpenGL
    sf::ContextSettings settings;
    // Устанавливаем уровень сглаживания краев
    // 8 - высокий уровень сглаживания для плавных краев полярной розы
    settings.antiAliasingLevel = 8;
    
    // Создаем окно с параметрами сглаживания
    // sf::RenderWindow - класс для создания и управления окном
    // sf::VideoMode({800, 600}) - размер окна: ширина 800, высота 600
    // "Polar Rose" - заголовок окна
    // sf::State::Windowed - состояние окна (в SFML 3 используется State вместо Style)
    // settings - параметры контекста (сглаживание)
    sf::RenderWindow window(
        sf::VideoMode({800, 600}), "Polar Rose",
        sf::State::Windowed, settings);
    
    // === СОЗДАНИЕ ПОЛЯРНОЙ РОЗЫ ЧЕРЕЗ ConvexShape ===
    // Полярная роза - это кривая, заданная в полярных координатах
    // Формула: r = a * sin(n * angle), где n - количество лепестков
    // sf::ConvexShape - класс для рисования выпуклых многоугольников
    // Примечание: полярная роза не является выпуклой фигурой, но SFML может ее нарисовать
    sf::ConvexShape rose;
    
    // Количество точек для аппроксимации розы
    // 500 точек - очень высокое качество, плавная кривая
    const int pointCount = 500;
    
    // Устанавливаем количество вершин многоугольника
    // setPointCount() - задает количество точек для фигуры
    rose.setPointCount(pointCount);
    
    // Параметры полярной розы
    // 6 - количество лепестков розы (можно изменить для другого количества)
    const int petals = 6;
    // 200.0f - максимальный радиус розы в пикселях
    const float maxRadius = 200.0f;
    
    // Цикл для создания точек полярной розы
    // Используем полярные координаты для создания формы розы
    for (int i = 0; i < pointCount; ++i)
    {
        // Вычисляем угол для текущей точки
        // 2 * M_PI - полный оборот в радианах (360 градусов)
        // i / pointCount - доля оборота для текущей точки (от 0 до 1)
        // 2 * M_PI * i / pointCount - угол в радианах для точки i
        float angle = 2.0f * static_cast<float>(M_PI) * i / static_cast<float>(pointCount);
        
        // Вычисляем радиус для текущего угла по формуле полярной розы
        // Формула: r = maxRadius * sin(petals * angle)
        // std::sin() - функция синуса из библиотеки <cmath>
        // petals * angle - умножение угла на количество лепестков
        // std::abs() - абсолютное значение (модуль), чтобы радиус был положительным
        // Это создает форму розы с заданным количеством лепестков
        float radius = maxRadius * std::abs(std::sin(petals * angle));
        
        // Переводим полярные координаты в декартовы (прямоугольные)
        // Полярные координаты: (угол, радиус)
        // Декартовы координаты: (x, y)
        // Формула перевода:
        // x = radius * cos(angle) - горизонтальная координата
        // y = radius * sin(angle) - вертикальная координата
        sf::Vector2f point = {
            radius * std::cos(angle),  // X координата точки
            radius * std::sin(angle)   // Y координата точки
        };
        
        // Устанавливаем координаты точки с номером i
        // setPoint() - задает координаты вершины многоугольника
        // i - номер точки (от 0 до pointCount-1)
        // point - координаты точки в виде sf::Vector2f
        rose.setPoint(i, point);
    }
    
    // Устанавливаем цвет заливки розы
    // sf::Color(0xFF, 0x09, 0x80) - розовый цвет в формате RGB
    // 0xFF - красный компонент (255 в десятичной системе)
    // 0x09 - зеленый компонент (9 в десятичной системе)
    // 0x80 - синий компонент (128 в десятичной системе)
    // 0x - префикс для шестнадцатеричных чисел
    rose.setFillColor(sf::Color(0xFF, 0x09, 0x80));
    
    // Переменные для анимации движения розы
    // time - переменная времени для создания анимации
    float time = 0.0f;
    // centerX, centerY - координаты центра движения розы
    // Инициализируем центром окна
    float centerX = 400.0f;  // Центр окна по X (800/2)
    float centerY = 300.0f;  // Центр окна по Y (600/2)
    // radius - радиус окружности, по которой движется роза
    const float orbitRadius = 150.0f;
    
    // === ОСНОВНОЙ ЦИКЛ ПРОГРАММЫ (GAME LOOP) ===
    // Цикл работает, пока окно открыто
    while (window.isOpen())
    {
        // === ЭТАП 1: ОБРАБОТКА СОБЫТИЙ ===
        // Цикл обработки всех событий из очереди
        // window.pollEvent() - извлекает событие из очереди (SFML 3 API)
        while (auto event = window.pollEvent())
        {
            // Проверяем, является ли событие закрытием окна
            if (event->is<sf::Event::Closed>())
            {
                // Закрываем окно, если пользователь нажал на крестик
                window.close();
            }
        }
        
        // === ЭТАП 2: ОБНОВЛЕНИЕ СОСТОЯНИЯ ===
        // Увеличиваем время для создания анимации движения
        // 0.005f - скорость изменения времени (уменьшена для более медленного движения)
        // Чем больше значение, тем быстрее роза движется по окружности
        time += 0.005f;
        
        // Вычисляем новую позицию розы по окружности
        // Используем тригонометрические функции для движения по кругу
        // orbitRadius - радиус окружности, по которой движется роза
        // time - угол, который увеличивается со временем
        // std::cos(time) и std::sin(time) - координаты точки на окружности
        // centerX, centerY - центр окна (точка, вокруг которой движется роза)
        float x = centerX + orbitRadius * std::cos(time);
        float y = centerY + orbitRadius * std::sin(time);
        
        // Устанавливаем новую позицию розы
        // setPosition() - обновляет координаты фигуры
        rose.setPosition({x, y});
        
        // === ЭТАП 3: ОТРИСОВКА ===
        // Очищаем окно белым цветом перед отрисовкой нового кадра
        // window.clear() - заполняет все окно указанным цветом
        window.clear(sf::Color::White);
        
        // Рисуем полярную розу на окне
        // window.draw() - добавляет фигуру в буфер отрисовки
        window.draw(rose);
        
        // Отображаем все нарисованное на экране
        // window.display() - выводит содержимое буфера на экран
        window.display();
    }
    
    // Возвращаем 0 - код успешного завершения программы
    return 0;
}

