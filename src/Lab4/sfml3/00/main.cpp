// Подключаем библиотеку для работы с вводом/выводом
// iostream - input/output stream, позволяет выводить данные в консоль
#include <iostream>

// Главная функция программы - точка входа
// int main() - функция, с которой начинается выполнение программы
int main()
{
    // Объявляем переменную num типа int и инициализируем значением 0
    // int - целочисленный тип данных
    // num - имя переменной
    // = 0 - присваивание начального значения
    int num = 0;
    
    // Цикл while - цикл с условием
    // Синтаксис: while (условие) { тело цикла }
    // Цикл выполняется, пока условие истинно (true)
    // num < 100 - условие: пока num меньше 100
    // Обратите внимание: нет точки с запятой после условия!
    // Если поставить точку с запятой, цикл станет бесконечным
    while (num < 100)
    {
        // Проверяем, кратно ли число 15 (т.е. и 3, и 5 одновременно)
        // Оператор % - деление с остатком (модуль)
        // num % 15 - остаток от деления num на 15
        // == 0 - сравнение на равенство (остаток равен нулю)
        // Если остаток равен 0, значит число кратно 15
        if (num % 15 == 0)
        {
            // Выводим "FizzBuzz" если число кратно 15
            // std::cout - стандартный поток вывода (console output)
            // << - оператор вставки, отправляет данные в поток
            // std::endl - переводит курсор на новую строку и сбрасывает буфер
            std::cout << "FizzBuzz" << std::endl;
        }
        // else if - альтернативное условие
        // Проверяется только если предыдущее условие ложно
        // Проверяем, кратно ли число 3
        else if (num % 3 == 0)
        {
            // Выводим "Fizz" если число кратно 3
            std::cout << "Fizz" << std::endl;
        }
        // Проверяем, кратно ли число 5
        else if (num % 5 == 0)
        {
            // Выводим "Buzz" если число кратно 5
            std::cout << "Buzz" << std::endl;
        }
        // else - блок выполняется, если все предыдущие условия ложны
        else
        {
            // Выводим само число, если оно не кратно ни 3, ни 5
            std::cout << num << std::endl;
        }
        
        // Увеличиваем num на 1 после каждой итерации цикла
        // Оператор += это сокращение от num = num + 1
        // Это важно! Без этого цикла num всегда будет равен 0, и цикл станет бесконечным
        num += 1;
    }
    
    // Возвращаем 0 - код успешного завершения программы
    return 0;
}

