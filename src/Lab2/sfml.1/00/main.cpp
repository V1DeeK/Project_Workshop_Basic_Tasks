// Подключаем библиотеку для работы с вводом/выводом (iostream - input/output stream)
#include <iostream>

// Главная функция программы - точка входа
int main()
{
	// Объявляем переменную a типа int (целое число) и инициализируем значением 0
	int a = 0;
	// Объявляем переменную b типа int (целое число) и инициализируем значением 0
	int b = 0;
	// Выводим на экран приглашение для ввода двух чисел
	// std::cout - стандартный поток вывода (console output)
	// std::endl - переводит курсор на новую строку и сбрасывает буфер
	std::cout << "enter two numbers:" << std::endl;
	// Считываем два числа из стандартного потока ввода (stdin)
	// std::cin - стандартный поток ввода (console input)
	// Оператор >> извлекает данные из потока и записывает в переменные
	std::cin >> a >> b;
	// Выводим на экран текст "sum: " и результат сложения переменных a и b
	// Оператор << вставляет данные в поток вывода
	std::cout << "sum: " << (a + b) << std::endl;
	// Возвращаем 0 - код успешного завершения программы
	// В C++ можно не писать return 0; в конце main(), компилятор добавит автоматически
}
